package logic.beans;

public class SearchBean {

    private String chefName;
    private String chefBestDish;
    private String chefCity;

    public String getChefName() {
        return chefName;
    }

    public void setChefName(String chefName) {
        this.chefName = chefName;
    }

    public String getChefBestDish() {
        return chefBestDish;
    }

    public void setChefBestDish(String chefBestDish) {
        this.chefBestDish = chefBestDish;
    }

    public String getChefCity() {
        return chefCity;
    }

    public void setChefCity(String chefCity) {
        this.chefCity = chefCity;
    }
}
